// Fast Example--------------------
var weekDay = 'Sunday';
switch(weekDay){
    case 'Saturday':
        console.log('Saturday! Today is closed');
        break;
    case 'Sunday':
        console.log('Sunday! It is normal day');
        break;
    case 'Monday':
        console.log('Monday! It is normal day');
        break;
    case 'Tuesday':
        console.log('Tuesday! It is normal day');
        break;
    case 'Wednesday':
        console.log('Wednesday! It is normal day');
        break;
    case 'Thursday':
        console.log('Thursday! It is a normal day');
        break;
    case 'Friday':
        console.log('Friday! It is a normal day');
        break;
    default:
        console.log('Not a Day');
}

